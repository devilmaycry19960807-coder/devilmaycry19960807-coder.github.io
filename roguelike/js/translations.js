// 获取翻译内容的工具函数
export function getContentTranslation(type, key, lang) {
    if (!contentTranslations[lang]) {
        console.warn(`Language ${lang} not found`);
        return key;
    }

    const translations = contentTranslations[lang][type];
    if (!translations) {
        console.warn(`Translation type ${type} not found`);
        return key;
    }

    return translations[key] || key;
}

// 获取装备翻译
export function getEquipmentTranslation(itemName, lang) {
    // 尝试从所有装备类型中查找
    const types = ['helmets', 'armors', 'weapons', 'gloves', 'boots', 'necklaces', 'rings'];
    for (const type of types) {
        const translated = getContentTranslation(type, itemName, lang);
        if (translated !== itemName) {
            return translated;
        }
    }
    return itemName;
}

// 获取遗物翻译
export function getArtifactTranslation(artifactName, lang, field = 'name') {
    const artifact = getContentTranslation('artifacts', artifactName, lang);
    if (typeof artifact === 'object' && artifact[field]) {
        return artifact[field];
    }
    if (typeof artifact === 'string') {
        return artifact;
    }
    return artifactName;
}

// 获取技能翻译
export function getSpellTranslation(spellId, lang, field = 'name') {
    const spell = getContentTranslation('spells', spellId, lang);
    if (typeof spell === 'object' && spell[field]) {
        return spell[field];
    }
    if (typeof spell === 'string') {
        return spell;
    }
    return spellId;
}

// 获取怪物翻译
export function getMonsterTranslation(monsterName, lang) {
    return getContentTranslation('monsters', monsterName, lang);
}

// 获取怪物描述翻译
export function getMonsterDescriptionTranslation(desc, lang) {
    return getContentTranslation('monsterDescriptions', desc, lang);
}
